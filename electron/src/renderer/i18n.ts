export function t(locale: LongclawLocale, key: string): string {
  return messages[locale][key] ?? messages['en-US'][key] ?? key
}

export function tf(
  locale: LongclawLocale,
  key: string,
  params: Record<string, string | number | undefined>,
): string {
  return t(locale, key).replace(/\{(\w+)\}/g, (_, name: string) => {
    const value = params[name]
    return value === undefined ? '' : String(value)
  })
}

export function humanizeTokenLocale(locale: LongclawLocale, value?: string | null): string {
  if (!value) return locale === 'zh-CN' ? '未知' : 'Unknown'
  const normalized = String(value)
  const localized = tokenLabels[locale][normalized]
  if (localized) return localized
  return normalized
    .replace(/_/g, ' ')
    .replace(/\b\w/g, char => char.toUpperCase())
}
